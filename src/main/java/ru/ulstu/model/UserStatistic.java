package ru.ulstu.model;

public class UserStatistic {

    private String login;

    public UserStatistic(String login) {
        this.login = login;
    }

    public String getLogin() {
        return login;
    }

    public void setLogin(String login) {
        this.login = login;
    }

    @Override
    public String toString() {
        return login;
    }
}

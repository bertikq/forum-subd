package ru.ulstu.model;

public class ModeratorStatistic {

    private String name;

    public ModeratorStatistic(String name) {
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Override
    public String toString() {
        return name;
    }
}
